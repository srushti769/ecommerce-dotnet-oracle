-- VIEW for Reports
CREATE OR REPLACE VIEW V_ORDER_SUMMARY AS
SELECT O.ORDER_ID,U.USERNAME,U.EMAIL,O.TOTAL_AMOUNT,O.STATUS,O.PAYMENT_METHOD,O.ORDER_DATE
FROM ORDERS O JOIN USERS U ON O.USER_ID=U.USER_ID;
